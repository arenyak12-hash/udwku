    <h4>Perjalanan UDW</h4>
    <p>Pada tanggal 19 September 2024, Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi mengeluarkan Keputusan Menteri Nomor 603/E/0/2024
    yang memberikan izin pendirian Universitas Dharma Wacana (UDW). Keputusan ini menandai tonggak sejarah penting bagi Yayasan Pendidikan dan 
    Kebudayaan Dharma Wacana (YPKDW) yang telah mengelola empat Perguruan Tinggi Swasta (PTS) di bawah naungannya.</p>
    
    <h4>Latar Belakang Penggabungan</h4>
    <p>Sejak tahun 2020, ide untuk menggabungkan empat PTS tersebut telah muncul. Ketua Lembaga Layanan Pendidikan Tinggi (LLDIKTI) Wilayah II,
    Prof. Yuliansyah, bersama Ketua YPKDW, Bapak Bima Uramanda, memulai diskusi mengenai kemungkinan penggabungan ini. Namun, realisasi ide 
    tersebut baru dapat terwujud pada tahun 2023, saat Prof. Ishaq menjabat sebagai Ketua LLDIKTI Wilayah II dan Bapak Rohmad Hariadi sebagai Ketua YPKDW.</p>
    
    <h4>Proses Penggabungan</h4>
    <p>Proses penggabungan ini tidak berjalan mulus. Diperlukan empat kali pendampingan dari DIKTI antara tahun 2023 hingga 2024 untuk memastikan 
    bahwa penggabungan ini sesuai dengan regulasi yang berlaku. Setiap tahap pendampingan menghadirkan tantangan tersendiri, mulai dari penyesuaian
    kurikulum hingga integrasi sistem administrasi.</p>
    
