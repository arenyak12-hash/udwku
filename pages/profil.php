    <p>Setelah melalui berbagai proses dan persiapan, akhirnya terbentuklah Universitas Dharma Wacana dengan empat fakultas utama:</p>
    <li>Fakultas Pertanian (FP): Menggabungkan program studi Agroteknologi dan Agribisnis.</li>
    <li>Fakultas Ilmu Sosial dan Ilmu Politik (FISIP): Menyelenggarakan program studi Administrasi Publik.</li>
    <li>Fakultas Kesehatan (FIKes): Menawarkan program studi D3 Keperawatan.</li>
    <li>Fakultas Teknologi Bisnis dan Sains (FTBS): Menyediakan program studi Teknik Informatika, Sistem Informasi, dan Bisnis Digital.</li>
    <p>Pembentukan fakultas-fakultas ini bertujuan untuk menyediakan program studi yang lebih beragam dan relevan dengan kebutuhan pasar kerja saat ini.</p>
    
